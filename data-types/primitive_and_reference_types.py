# primitive types

message = 'Hello Python!'
print(message)

num = 123456
print(num)

pi = 3.141592654
print(pi)

checked = True
print(checked)

empty = None
print(empty)

# reference types

list_of_countries = ['Bangladesh', 'India', 'Pakistan']
print(list_of_countries)

tuple_of_countries = ('Bangladesh', 'India', 'Pakistan')
print(tuple_of_countries)

dict_of_countries = {88: 'Bangladesh', 91: 'India', 92: 'Pakistan'}
print(dict_of_countries)

set_of_countries = set(['Bangladesh', 'India', 'Pakistan'])
print(set_of_countries)