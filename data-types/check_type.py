# check types

message = 'Hello Python!'
print(type(message))

num = 1234
print(type(num))

pi = 3.141592654
print(type(pi))

checked = True
print(type(checked))

empty = None
print(type(empty))

list_of_countries = ['Bangladesh', 'India', 'Pakistan']
print(type(list_of_countries))

tuple_of_countries = ('Bangladesh', 'India', 'Pakistan')
print(type(tuple_of_countries))

dict_of_countries = {88: 'Bangladesh', 91: 'India', 92: 'Pakistan'}
print(type(dict_of_countries))

set_of_countries = set(['Bangladesh', 'India', 'Pakistan'])
print(type(set_of_countries))