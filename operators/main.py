# arithmetic operators
print(2 + 3)
print(5 - 3)
print(2 * 3)
print(5 / 3)
print(5 % 3)

# assignment operators
first = 2
print(first)

first += 3
print(first)

# comparison operators
print(2 < 2)
print(2 <= 2)
print(2 > 2)
print(2 >= 2)

# equality operators
print(2 == 3)
print(2 != 3)

# binary logical operators
print(2 < 3 and 3 < 2)
print(2 < 3 or 3 < 2)
print(not 2 < 3)

# identity operators
print(2 + 3 is 5)
print(2 + 3 is not 5)

# membership operators
print('b' in ['a', 'b', 'c'])
print('b' not in ['a', 'b', 'c'])